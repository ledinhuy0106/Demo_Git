package model;

public class Student {
    private int id;
    private int age;
    private String name;
    private String image;

    public Student(int id, int age, String name, String image) {
        this.id = id;
        this.age = age;
        this.name = name;
        this.image = image;
    }

    public Student(int age, String name, String image) {
        this.age = age;
        this.name = name;
        this.image = image;
    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
